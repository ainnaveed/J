// While Loop
            // Print tables from 1 to 10
let num = 1;
while (num <= 10) {
    console.log(`Multiplication Table for ${num}:`);
    let i = 1;
    while (i <= 10) {
        console.log(`${num} x ${i} = ${num * i}`);
        i++;
    }
    console.log("\n");
    num++;
}

