console.log("Operators in Js")
                            // Arithmetic Operators
let a = 30;
let b = 5;
// Addition Operator
console.log("a + b =", a+b);
// Subtraction Operator
console.log("a - b =", a-b);
// Multiplication Operator
console.log("a * b =", a*b);
// Division Operator
console.log("a / b =", a/b);
// Modulus Operator
console.log("a % b =", a%b);
// Power Operator
console.log("a ** b =", a**b);
// Increment Operator
console.log("a ++ =", a++);
console.log("a =", a);
console.log("++ a  =", ++a);
console.log("a =", a);
console.log("b ++ =", b++);
console.log("b =", b);
console.log("++ b =", ++b);
console.log("b =", b);
// Decrement Operator
console.log("a -- =", a--);
console.log("a =", a);
console.log("-- a =", --a);
console.log("a =", a);
console.log("b -- =", b--);
console.log("b =", b);
console.log("-- b =", --b);
console.log("b =", b);

                            // Assignment Operators
let c = 20;
c+=5; // same as c = c+5
console.log("c =",c);
c-=6; // same as c = c-6
console.log("c =",c);
c*=6; // same as c = c*6
console.log("c =",c);
c/=6; // same as c = c/6
console.log("c =",c);
c%=6; // same as c = c%6
console.log("c =",c);
c**=6; // same as c = c**6
console.log("c =",c);

                            // Comparison Operator
comp1 = 20;
comp2 = 10;
console.log("comp1 == comp2 =", comp1==comp2);
console.log("comp1 < comp2 =", comp1<comp2);
console.log("comp1 > comp2 =", comp1>comp2);
console.log("comp1 <= comp2 =", comp1<=comp2);
console.log("comp1 >= comp2 =", comp1>=comp2);
console.log("comp1 === comp2 =", comp1===comp2);
console.log("comp1 !== comp2 =", comp1!==comp2);
console.log("comp1 != comp2 =", comp1!=comp2);

                            // Logical Operators
x = 10;
y = 20;
console.log(x>y && x<y);
console.log(x>y || x<y);
console.log(!(x<y));